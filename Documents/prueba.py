def invertircolores(felix):
    for i in range(len(felix)):
        for j in range(len(felix[0])):
            if felix[i][j] == 1:
                felix[i][j] = 0
            else:
                felix[i][j] = 1
    return felix

def mirrorv(felix):
    ren = len(felix)-1
    for i in range(17):
        for j in range(len(felix[0])):
            felix[ren][j] = felix[i][j]
        ren -= 1
    return felix

def mirrorh(felix):
    col = len(felix)-1
    for i in range(len(felix)):
        for j in range(17):
            felix[i][col] = felix[i][j]
            col -= 1
        col = len(felix)-1 
    return felix

